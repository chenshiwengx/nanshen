package com.bolo.entity;

public class Childtalk {

	private int id;
	private String content;
	private int userid;
	private int maintalkid;
	private int pid;
	private String img;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public int getMaintalkid() {
		return maintalkid;
	}
	public void setMaintalkid(int maintalkid) {
		this.maintalkid = maintalkid;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
}
